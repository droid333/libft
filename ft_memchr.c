#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*ptr;

	i = 0;
	ptr = (unsigned char *)s;
	while (ptr[i] && i < n)
	{
		if (ptr[i] == c)
			return (ptr + i);
		i++;
	}
	return (0x0);
}
